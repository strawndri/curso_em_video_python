def aumentar(preco):
    novo_preco = preco + (preco * 0.10)
    return novo_preco


def diminuir(preco):
    novo_preco = preco - (preco * 0.10)
    return novo_preco


def dobro(preco):
    novo_preco = preco * 2
    return novo_preco


def metade(preco):
    novo_preco = preco /2
    return novo_preco